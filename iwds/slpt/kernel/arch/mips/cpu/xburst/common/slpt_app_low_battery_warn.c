enum low_bat_res_order {
	LOW_BAT_WARN,
	LOW_BAT_LOW,
	LOW_BAT_SHUTDOWN,
	LOW_BAT_SEPARATOR,

	/* KEEP LAST */
	LOW_BAT_RES_NUMS,
};

struct low_battery_warn {
	char bmp[LOW_BAT_RES_NUMS][8192];
	struct position pos;
	struct img_display img;
	struct color_mode *colors[LOW_BAT_RES_NUMS];
	struct {
		unsigned int is_ok:1;
	} s;
};

extern struct low_battery_warn low_bat;

static struct slpt_app_res low_bat_res[] = {
	[LOW_BAT_WARN] = SLPT_RES_ARR_DEF("low-bat-warn", low_bat.bmp[LOW_BAT_WARN]),
	[LOW_BAT_LOW] = SLPT_RES_ARR_DEF("low-bat-low", low_bat.bmp[LOW_BAT_LOW]),
	[LOW_BAT_SHUTDOWN] = SLPT_RES_ARR_DEF("low-bat-shutdown", low_bat.bmp[LOW_BAT_SHUTDOWN]),
	[LOW_BAT_SEPARATOR] = SLPT_RES_ARR_DEF("low-bat-separator", low_bat.bmp[LOW_BAT_SEPARATOR]),
};

static struct slpt_app_res low_bat_config_res[] = {
	SLPT_RES_INT_DEF("low-bat-pos-x", low_bat.pos.x),
	SLPT_RES_INT_DEF("low-bat-pos-y", low_bat.pos.y),
};

SLPT_REGISTER_RES_ARR(low_bat_res, low_bat_res);
SLPT_REGISTER_RES_ARR(low_bat_config, low_bat_config_res);

static int low_bat_init(void) {
	int ret;

	ret = img_display_init(&low_bat.img, low_bat.bmp, ARRAY_SIZE(low_bat.bmp[0]), ARRAY_SIZE(low_bat.bmp), &low_bat.pos);
	if (ret) {
		pr_err("SLPT: error: failed to init low battery img \n");
		goto retun_ret;
	}

retun_ret:
	return ret;
}
SLPT_APP_INIT_ONETIME(low_bat_init);

void low_battery_display(int low_battery_level) {
	static int count = 0;

	if (low_battery_level == SLPTARG_MODE_WARNING)
		low_battery_level = LOW_BAT_WARN;
	else if (low_battery_level == SLPTARG_MODE_LOWPOWER) {
		if (slpt_low_power_shutdown)
			low_battery_level = LOW_BAT_SHUTDOWN;
		else
			low_battery_level = LOW_BAT_LOW;
	} else
		low_battery_level = LOW_BAT_SEPARATOR;

	low_battery_level = count++ % 2 ? LOW_BAT_SEPARATOR : low_battery_level;

	pr_debug("low battery level: %d\n", low_battery_level);

	img_display_set_index(&low_bat.img, low_battery_level);
	img_display(&low_bat.img);
}

static int cmd_low_bat_show(cmd_tbl_t *cmdtp, int flag, int argc, char * const argv[]) {
	unsigned int index;

	struct fb_struct *fbs = get_default_fb();

	if (!fbs) {
		pr_err("Failed to get default fb\n");
		return -ENOMEM;
	}

	if (argc < 2) {
		index = 0;
	} else {
		index = simple_strtoul(argv[1], NULL, 10);
	}

	printf("index is : %u\n", index);
	img_display_set_index(&low_bat.img, index);
	img_display(&low_bat.img);

	lcd_pan_display(fbs, 0);
	return 0;
}

